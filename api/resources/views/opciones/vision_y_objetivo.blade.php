    <p class="text"><b>TÍDELIB (TIENDA DE LIBROS) 
    ES UNA PLATAFORMA DIGITAL INNOVADORA, UN 
    ESPACIO VIRTUAL, CREADO ESPECIALMENTE PARA LECTORES, 
    ESCRITORES, EDITORIALES Y ORGANIZACIONES AFINES.</b> 
    Está orientada a la venta y 
    promoción de obras literarias digitales, que han sido 
    tanto coproducidas como autopublicadas, 
    brindando un entorno donde la creatividad y la 
    literatura pueden florecer. Tídelib es un lugar, donde 
    los autores tienen la oportunidad de presentar sus trabajos al 
    mundo y los lectores, explorar y descubrir nuevas voces 
    y relatos.</p>
    <br>
    <p class="text">Trabajamos en por de fomentar una relación directa y 
    enriquecedora entre los autores y sus lectores, estableciendo 
    como propuesta general la creación de lazos significativos 
    entre escritores, creadores, productores y editoriales. Esta interacción 
    es fundamental, ya que Tídelib, no solo actúa como un mercado para obras 
    publicadas, sino también, se esfuerza por acercar libros digitales de 
    todos los géneros literarios a los lectores contemporáneos, 
    adaptándose a sus comodidades, gustos y preferencias. De este modo, la 
    plataforma se convierte en un nexo fortalecedor entre el creador y su público, 
    acrecentando el tráfico, en la ruta del arte.</p>
    <br>
    <p class="text">Comprar y/o vender obras en Tídelib, es una experiencia 
      nueva y diferente, cada acción comercial retribuye directamente a los 
      respectivos creadores y productores, responsables de la inversión. 
      Esto significa que los autores tienen la oportunidad de obtener el 
      100% de las ganancias generadas en las ventas de sus obras, lo que 
      resulta, en un modelo de negocio justo y sostenible. El espíritu 
      comunitario de Tídelib, se basa en la autonomía y en las relaciones
       humanas, priorizando la conexión genuina entre todos los 
       involucrados en el proceso de compra y venta. </p>
    <br>
    <p class="text">La plataforma tiene una interfaz de usuario donde 
    cada persona puede registrarse gratis y vender uno o más obras, de 
    manera automática, vinculando su propia cuenta de banco. </p>
    <br>
    <p class="text">En cuanto a los perfiles de las publicaciones, 
    los visitantes pueden comentar sobre las obras que conozcan y 
    dar like (me gusta) a los libros de sus preferencias. En Tídelib, 
    todo autor tiene la posibilidad de ofrecer un intercambio de su 
    libro, a otro creador, con solo un clic; de esta manera, se 
    potencia al margen de toda actividad comercial, una relación 
    más estrecha en la comunidad de escritores. La plataforma es 
    una red de obras y autores de habla hispana, que concede el 
    primer lugar a las mismas obras y sus propiedades literarias, 
    y en efecto, fortaleciendo el desarrolo de los distintos géneros y subgéneros.</p>
    <br>
    <p class="text">"Vamos, no pierdas la oportunidad de unirte a la familia del arte, 
    en Tidelib".</p>
